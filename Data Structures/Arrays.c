/*

An array is a collection of items stored at contiguous memory locations. 
The idea is to store multiple items of the same type together.

Types of arrays : 
1) One-dimensional array (1-D arrays)
2) Multidimensional array

1)-------------------- 1-D array --------------------------
0 - (zero-based indexing): The first element of the array is indexed by a subscript of 0.
1 - (one-based indexing): The first element of the array is indexed by the subscript of 1.
n - (N-based indexing): The base index of an array can be freely chosen.

int arr[ 5 ] = {  };
int arr[ 5 ] = { 1, 2, 3, 4, 5 };

2)------------------- 2-D array ---------------------------

A multi-dimensional array can be termed as an array of arrays that stores homogeneous data
in tabular form. Data in multidimensional arrays is generally stored in row-major order in the memory.

Two dimensional array: int two_d[10][20];
Three dimensional array: int three_d[10][20][30];

*/